package com.naruto.junit.service;


/**
 * 数学业务类
 *
 * @Author: naruto
 * @CreateTime: 2024-11-03-16:58
 */
public class MathService {
    /**
     * 求和方法
     *
     * @param a
     * @param b
     * @return
     */
    public int sum(int a, int b) {
        return a + b;
    }

    /**
     * 相减方法
     *
     * @param a
     * @param b
     * @return
     */
    public int sub(int a, int b) {
        return a - b;
    }
}
